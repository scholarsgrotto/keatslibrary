            <p>The Keats Library is a digital humanities project launched by Daniel Johnson (University of Notre Dame Libraries), Greg Kucich (University of Notre Dame, English Department), and Beth Lau (CSU-Long Beach, English Department, Emerita) in July 2018, with the initial goal of producing a scholarly edition of <a href="paradise-lost/">John Keats's heavily annotated copy of <span class="titleAttribution">Paradise Lost</span></a>. Dr. Lau, in her 1994 print edition of Keats's <span class="titleAttribution">Paradise Lost</span> outlined the challenges of producing a physical book of this nature &mdash; the economics of academic publishing would allow a reconstruction only of those lines Keats annotated, not the entirety of the poem. Readers hoping to take in the full context of Keats's markings would need to juggle a second, full copy of the <span class="titleAttribution">Paradise Lost</span>. The digital environment eliminates this cost/size barrier, and thanks to helpful collaboration with the <a href="https://www.cityoflondon.gov.uk/things-to-do/keats-house/Pages/default.aspx">Keats House</a>, we were able to obtain high resolution page scans of the original volumes, to display side by side with the transcription and encoding. 
