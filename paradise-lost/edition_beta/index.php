			<h3>Scholarly Introduction</h3>
			
			<p><span class="mini-header">*As of 2018-10, Beth Lau's introduction is undergoing revision. During this process, a very brief preview is offered in its place below. Anyone wishing to quote the edition at this time should note that this is a beta release, or at the least, indicate date of access in a works cited. Changes to the <span class="titleAttribution">Paradise Lost</span> transcription/encoding will be documented in the "revisionDesc" section of the TEI-XML, while dates of change to the scholarly introduction will be noted below the introduction text.
			</span>

			<p>John Keats’s complete, two-volume copy of <span class="titleAttribution">Paradise Lost</span> is unique as a major work of English literature and formative influence on Keats’s poetry that is heavily marked and annotated throughout solely in Keats’s hand.  It provides a remarkable record, not just of the poet’s response to Milton’s epic poem, but of his reading practices, aesthetic tastes, and habits of mind more broadly.
			</p>
			
			<p>	Keats gave these volumes to Maria Dilke before he left for Italy; the second volume is inscribed “Mrs Dilke from / her sincere friend / J. Keats.”  Keats’s <span class="titleAttribution">Paradise Lost</span> remained in the Dilke family throughout the nineteenth century.  Both volumes contain C. W. Dilke’s bookplate, and they eventually were donated to the Hampstead Libraries by his grandson Sir Charles Dilke as part of the valuable Dilke bequest.  They now belong to Keats House in Hampstead and are housed in the London Metropolitan Archives. [...]

